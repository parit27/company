package com.company;

public class DataType {

	public static void main(String[] args) {
		System.out.println("------------Data Type----------");
		System.out.println("Example for Int");
		int num1 = 6;
		int num2 = 5;
		int num3 = 7;
		int sum = num1 + num2 + num3;
		System.out.println("Total of sum:"+sum);
		System.out.println();
		
		System.out.println("Example of Double");
		double num4 = 6.23;
		double num5 = 5.63;
		double num6 = 7.33;
		double sum1 = num4 + num5 + num6;
		System.out.println("Total of sum:"+sum1);
		System.out.println();
		
		System.out.println("Example of Float");
		float num7 = 6.22467f;
		float num8 = 5.64246f;
		float num9 = 7.36444f;
		float sum2 = num7 + num8 + num9;
		System.out.println("Total of sum:"+sum2);
		System.out.println();
		
		System.out.println("Example of Char");
		char char1='a';
		char char2='A';
		System.out.println("char1: "+char1);
		System.out.println("char2: "+char2);
		System.out.println();
		
		System.out.println("Example of Long");
		Long lg = 9978914755l;
		System.out.println("Total of Long:"+lg);
		System.out.println();
	
		System.out.println("Example of Boolean");
		boolean b = true;
		System.out.println("Total of Boolean:"+b);
		System.out.println();
	
		System.out.println("Example of String");
		String st = "Parit Trivedi";
		System.out.println("Total of String:"+st);
		System.out.println();

	}

}
